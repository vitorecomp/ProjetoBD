package Trabalho.Entidades;

public class Equipe {

    private int id;
    private int treinador;
    private int pais;
    private int copa;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTreinador() {
        return treinador;
    }

    public void setTreinador(int treinador) {
        this.treinador = treinador;
    }

    public int getPais() {
        return pais;
    }

    public void setPais(int pais) {
        this.pais = pais;
    }

    public int getCopa() {
        return copa;
    }

    public void setCopa(int copa) {
        this.copa = copa;
    }

   
}
